def addition(num1, num2):
    result = num1 + num2
    return result


def subtraction(num1, num2):
    result = num1 - num2
    return result


def multiplication(num1, num2):
    result = num1 * num2
    return result


def division(num1, num2):
    result = num1 / num2
    return result

def modulus(num1, num2):
    result = num1 % num2
    return result

def exponent(num1, num2):
    result = num1 ** num2
    return result